package vn.sesgroup.hddt.user.impl;

public class PoolData {
	  int number;
	  String string;
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getString() {
		return string;
	}
	public void setString(String string) {
		this.string = string;
	}
	public PoolData() {
		super();
	}
	public PoolData(int number, String string) {
		super();
		this.number = number;
		this.string = string;
	}
	  
	
}
